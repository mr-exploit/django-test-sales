CREATE TABLE "customers" (
  "CUSTOMER_ID" SERIAL PRIMARY KEY NOT NULL,
  "CUSTOMER_NAME" varchar(200) DEFAULT null
);

CREATE TABLE "products" (
  "PRODUCT_ID" SERIAL PRIMARY KEY NOT NULL,
  "PRODUCT_CODE" varchar(15) DEFAULT null,
  "PRODUCT_NAME" varchar(250) DEFAULT null,
  "PRODUCT_PRICE" float DEFAULT null,
  "PRODUCT_STATUS" varchar(11) DEFAULT '0',
  "PRODUCT_STOCK" int(11) DEFAULT '0'
);

CREATE TABLE "sale_items" (
  "ITEM_ID" SERIAL PRIMARY KEY NOT NULL,
  "SALE_ID" int(11) DEFAULT null,
  "PRODUCT_ID" int(11) DEFAULT null,
  "PRODUCT_PRICE" float DEFAULT null,
  "ITEM_QTY" int(11) DEFAULT '0',
  "IS_VERIFY" int(11) DEFAULT '0'
);

CREATE TABLE "sales" (
  "SALE_ID" SERIAL PRIMARY KEY NOT NULL,
  "SALE_DATE" datetime DEFAULT null,
  "CUSTOMER_ID" int(11) DEFAULT null,
  "SALE_ITEMS_TOTAL" int(11) DEFAULT '0'
);

CREATE INDEX "SALE_ID" ON "sale_items" ("SALE_ID");

CREATE INDEX "PRODUCT_ID" ON "sale_items" ("PRODUCT_ID");

CREATE INDEX "CUSTOMER_ID" ON "sales" ("CUSTOMER_ID");

ALTER TABLE "sale_items" ADD CONSTRAINT "sale_items_ibfk_1" FOREIGN KEY ("SALE_ID") REFERENCES "sales" ("SALE_ID");

ALTER TABLE "sale_items" ADD CONSTRAINT "sale_items_ibfk_2" FOREIGN KEY ("PRODUCT_ID") REFERENCES "products" ("PRODUCT_ID");

ALTER TABLE "sales" ADD CONSTRAINT "sales_ibfk_1" FOREIGN KEY ("CUSTOMER_ID") REFERENCES "customers" ("CUSTOMER_ID");





